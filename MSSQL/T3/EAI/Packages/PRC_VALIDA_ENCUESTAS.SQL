/*
    Homologo de EAI.PKG_ENCUESTAS_MKT.PRC_VALIDA_ENCUESTAS.

    Dev Jr - mapa de conversion:
    - #Trabajo reemplaza BULK COLLECT y conserva los valores crudos y convertidos.
    - TRY_CONVERT reemplaza TO_NUMBER con EXCEPTION por cada campo.
    - #Errores reemplaza las colecciones PL/SQL usadas por FORALL.
    - ROW_NUMBER genera las llaves porque las tablas migradas no tienen IDENTITY.
    - CARGADAS no posee una PK equivalente al ROWID. El update usa IDENCUESTADO,
      que debe ser unico en la carga. Si el negocio admite duplicados, agregue una
      llave tecnica a CARGADAS y cambie el JOIN final para usarla.
    - CUANTO_VENDE se conserva como NULL en VALIDAS/INVALIDAS: asi se comporta el
      body Oracle, donde la variable se declara pero nunca se asigna.
*/
SET ANSI_NULLS ON;
SET QUOTED_IDENTIFIER ON;
GO

CREATE OR ALTER PROCEDURE [EAI].[PRC_VALIDA_ENCUESTAS]
AS
BEGIN
    SET NOCOUNT ON;
    SET XACT_ABORT ON;

    DECLARE @Inicio DATETIME2(0) = SYSDATETIME();
    DECLARE @Ejecucion DECIMAL(30,0) = 1;
    DECLARE @Cargados BIGINT = 0, @Invalidos BIGINT = 0,
            @InvalidosInegi BIGINT = 0, @Validos BIGINT = 0;

    BEGIN TRY
        BEGIN TRANSACTION;

        TRUNCATE TABLE [EAI].[ENCUESTAS_CLIENTES_VALIDAS];
        DELETE FROM [EAI].[ENCUESTAS_CLIENTES_ERRORES];
        DELETE FROM [EAI].[ENCUESTAS_CLIENTES_INVALIDAS];
        DELETE FROM [EAI].[ENCUESTAS_CLIENTES_BITACORA];

        SELECT
            IDENTITY(BIGINT, 1, 1) AS [FILA], c.*,
            TRY_CONVERT(DECIMAL(30,0), NULLIF(LTRIM(RTRIM(c.[IDENCUESTADO])), '')) AS [N_IDENCUESTADO],
            TRY_CONVERT(DECIMAL(30,0), NULLIF(LTRIM(RTRIM(c.[IDENCUESTADOR])), '')) AS [N_IDENCUESTADOR],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[TERRITORIO])), '')) AS [N_TERRITORIO],
            TRY_CONVERT(DECIMAL(5,0),  NULLIF(LTRIM(RTRIM(c.[SEMANA])), '')) AS [N_SEMANA],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[CONSECUTIVO])), '')) AS [N_CONSECUTIVO],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[TELEFONO])), '')) AS [N_TELEFONO],
            TRY_CONVERT(DECIMAL(8,0),  NULLIF(LTRIM(RTRIM(c.[DATOS_GEN_CODIGOPOSTAL])), '')) AS [N_CODIGOPOSTAL],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[VENTA_SEM_PALLMALL])), '')) AS [N_PALLMALL],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[VENTA_SEM_LUCKYSTRIKE])), '')) AS [N_LUCKYSTRIKE],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[VENTA_SEM_SHOTS])), '')) AS [N_SHOTS],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[VENTA_SEM_CAMEL])), '')) AS [N_CAMEL],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[VENTA_SEM_OTRASBAT])), '')) AS [N_OTRASBAT],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[VENTA_SEM_MARLBORO])), '')) AS [N_MARLBORO],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[VENTA_SEM_BENSONHEADGES])), '')) AS [N_BENSONHEADGES],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[VENTA_SEM_DELICADOS])), '')) AS [N_DELICADOS],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[VENTA_SEM_OTRASPMI])), '')) AS [N_OTRASPMI],
            TRY_CONVERT(DECIMAL(10,0), NULLIF(LTRIM(RTRIM(c.[VENTA_SEM_OTRASMARCAS])), '')) AS [N_OTRASMARCAS]
        INTO #Trabajo
        FROM [EAI].[ENCUESTAS_CLIENTES_CARGADAS] AS c;

        SET @Cargados = @@ROWCOUNT;

        CREATE TABLE #Errores
        (
            [FILA] BIGINT NOT NULL,
            [CAMPO_ERROR] VARCHAR(120) NOT NULL,
            [MENSAJE_DETALLE] VARCHAR(2000) NOT NULL
        );

        /* Reglas obligatorias, catalogos y conversiones numericas. */
        INSERT INTO #Errores ([FILA], [CAMPO_ERROR], [MENSAJE_DETALLE])
        SELECT t.[FILA], v.[Campo], v.[Mensaje]
        FROM #Trabajo AS t
        CROSS APPLY
        (
            VALUES
            ('IDENCUESTADO', 'IDENCUESTADO es obligatorio y numerico',
                CASE WHEN NULLIF(LTRIM(RTRIM(t.[IDENCUESTADO])), '') IS NULL OR t.[N_IDENCUESTADO] IS NULL THEN 1 ELSE 0 END),
            ('IDENCUESTADOR', 'IDENCUESTADOR es obligatorio y numerico',
                CASE WHEN NULLIF(LTRIM(RTRIM(t.[IDENCUESTADOR])), '') IS NULL OR t.[N_IDENCUESTADOR] IS NULL THEN 1 ELSE 0 END),
            ('FECHA_INICIO', 'FECHA_INICIO es obligatoria', CASE WHEN t.[FECHA_INICIO] IS NULL THEN 1 ELSE 0 END),
            ('FECHA_FIN', 'FECHA_FIN es obligatoria', CASE WHEN t.[FECHA_FIN] IS NULL THEN 1 ELSE 0 END),
            ('DIRECCIONIP', 'DIRECCIONIP es obligatoria', CASE WHEN NULLIF(LTRIM(RTRIM(t.[DIRECCIONIP])), '') IS NULL THEN 1 ELSE 0 END),
            ('FUENTE', 'FUENTE debe ser INEGI o BARRIDO',
                CASE WHEN NULLIF(LTRIM(RTRIM(t.[FUENTE])), '') IS NULL OR UPPER(LTRIM(RTRIM(t.[FUENTE]))) NOT IN ('INEGI','BARRIDO') THEN 1 ELSE 0 END),
            ('VENDE_CIGARRO', 'VENDE_CIGARRO contiene un valor no permitido',
                CASE WHEN NULLIF(LTRIM(RTRIM(t.[VENDE_CIGARRO])), '') IS NULL OR UPPER(LTRIM(RTRIM(t.[VENDE_CIGARRO]))) NOT IN
                    ('0 - SI','1 - CERRADO PERMANENTE','2 - NO VENDE CIGARRO','3 - NO ESTA INTERESADO') THEN 1 ELSE 0 END),
            ('VISITADO_BAT', 'VISITADO_BAT debe ser SI o NO y BARRIDO no puede indicar SI',
                CASE WHEN (t.[FECHA_INICIO] > CONVERT(DATETIME2(0),'20161004',112) AND NULLIF(LTRIM(RTRIM(t.[VISITADO_BAT])), '') IS NULL)
                       OR (NULLIF(LTRIM(RTRIM(t.[VISITADO_BAT])), '') IS NOT NULL AND UPPER(LTRIM(RTRIM(t.[VISITADO_BAT]))) NOT IN ('SI','NO'))
                       OR (UPPER(LTRIM(RTRIM(t.[FUENTE])))='BARRIDO' AND UPPER(LTRIM(RTRIM(t.[VISITADO_BAT])))='SI') THEN 1 ELSE 0 END),
            ('COORDENADAS', 'COORDENADAS son obligatorias y distintas de 0 para BARRIDO',
                CASE WHEN UPPER(LTRIM(RTRIM(t.[FUENTE])))='BARRIDO' AND COALESCE(NULLIF(LTRIM(RTRIM(t.[COORDENADAS])), ''),'0')='0' THEN 1 ELSE 0 END),
            ('TIENE_EXHIBIDOR', 'TIENE_EXHIBIDOR contiene un valor no permitido',
                CASE WHEN NULLIF(LTRIM(RTRIM(t.[TIENE_EXHIBIDOR])), '') IS NOT NULL
                       AND UPPER(LTRIM(RTRIM(t.[TIENE_EXHIBIDOR]))) NOT IN ('NO TIENE','BAT','PMI','AMBOS') THEN 1 ELSE 0 END),
            ('TELEFONO', 'TELEFONO debe ser numerico',
                CASE WHEN NULLIF(LTRIM(RTRIM(t.[TELEFONO])), '') IS NOT NULL AND t.[N_TELEFONO] IS NULL THEN 1 ELSE 0 END),
            ('SEMANA', 'SEMANA es obligatoria, numerica y debe estar entre 1 y 53',
                CASE WHEN t.[N_SEMANA] IS NULL OR t.[N_SEMANA] NOT BETWEEN 1 AND 53 THEN 1 ELSE 0 END),
            ('DIA_VISITA', 'DIA_VISITA debe estar entre LUNES y SABADO',
                CASE WHEN UPPER(LTRIM(RTRIM(t.[DIA_VISITA]))) NOT IN ('LUNES','MARTES','MIERCOLES','MIÉRCOLES','JUEVES','VIERNES','SABADO','SÁBADO')
                       OR NULLIF(LTRIM(RTRIM(t.[DIA_VISITA])), '') IS NULL THEN 1 ELSE 0 END),
            ('CONSECUTIVO', 'CONSECUTIVO no cumple la regla de la fuente',
                CASE WHEN t.[N_CONSECUTIVO] IS NULL
                       OR (UPPER(LTRIM(RTRIM(t.[FUENTE])))='INEGI' AND t.[N_CONSECUTIVO] NOT BETWEEN 1 AND 99)
                       OR (UPPER(LTRIM(RTRIM(t.[FUENTE])))='BARRIDO' AND t.[N_CONSECUTIVO]<>0) THEN 1 ELSE 0 END),
            ('NOMBRE_CONTACTO', 'NOMBRE_CONTACTO es obligatorio para BARRIDO',
                CASE WHEN UPPER(LTRIM(RTRIM(t.[FUENTE])))='BARRIDO'
                       AND (NULLIF(LTRIM(RTRIM(t.[NOMBRE_CONTACTO])), '') IS NULL OR t.[NOMBRE_CONTACTO] LIKE '%No proporcion%') THEN 1 ELSE 0 END),
            ('DATOS_GEN_NOMBRETIENDA', 'DATOS_GEN_NOMBRETIENDA es obligatorio para BARRIDO',
                CASE WHEN UPPER(LTRIM(RTRIM(t.[FUENTE])))='BARRIDO' AND NULLIF(LTRIM(RTRIM(t.[DATOS_GEN_NOMBRETIENDA])), '') IS NULL THEN 1 ELSE 0 END),
            ('DATOS_GEN_CALLEYNUMERO', 'DATOS_GEN_CALLEYNUMERO es obligatorio para BARRIDO',
                CASE WHEN UPPER(LTRIM(RTRIM(t.[FUENTE])))='BARRIDO' AND NULLIF(LTRIM(RTRIM(t.[DATOS_GEN_CALLEYNUMERO])), '') IS NULL THEN 1 ELSE 0 END),
            ('DATOS_GEN_COLONIAYCIUDAD', 'DATOS_GEN_COLONIAYCIUDAD es obligatorio para BARRIDO',
                CASE WHEN UPPER(LTRIM(RTRIM(t.[FUENTE])))='BARRIDO' AND NULLIF(LTRIM(RTRIM(t.[DATOS_GEN_COLONIAYCIUDAD])), '') IS NULL THEN 1 ELSE 0 END),
            ('DATOS_GEN_CODIGOPOSTAL', 'DATOS_GEN_CODIGOPOSTAL debe ser numerico',
                CASE WHEN NULLIF(LTRIM(RTRIM(t.[DATOS_GEN_CODIGOPOSTAL])), '') IS NOT NULL AND t.[N_CODIGOPOSTAL] IS NULL THEN 1 ELSE 0 END),
            ('CLIENTE_POTENCIAL', 'CLIENTE_POTENCIAL debe ser SI o NO',
                CASE WHEN NULLIF(LTRIM(RTRIM(t.[CLIENTE_POTENCIAL])), '') IS NOT NULL AND UPPER(LTRIM(RTRIM(t.[CLIENTE_POTENCIAL]))) NOT IN ('SI','NO') THEN 1 ELSE 0 END),
            ('TERRITORIO', 'TERRITORIO es obligatorio y numerico',
                CASE WHEN t.[N_TERRITORIO] IS NULL THEN 1 ELSE 0 END)
        ) AS v([Campo], [Mensaje], [Invalido])
        WHERE v.[Invalido] = 1;

        /* Las diez ventas semanales comparten la misma regla numerica opcional. */
        INSERT INTO #Errores ([FILA], [CAMPO_ERROR], [MENSAJE_DETALLE])
        SELECT t.[FILA], v.[Campo], v.[Campo] + ' debe ser numerico'
        FROM #Trabajo AS t
        CROSS APPLY
        (
            VALUES
            ('VENTA_SEM_PALLMALL', t.[VENTA_SEM_PALLMALL], t.[N_PALLMALL]),
            ('VENTA_SEM_LUCKYSTRIKE', t.[VENTA_SEM_LUCKYSTRIKE], t.[N_LUCKYSTRIKE]),
            ('VENTA_SEM_SHOTS', t.[VENTA_SEM_SHOTS], t.[N_SHOTS]),
            ('VENTA_SEM_CAMEL', t.[VENTA_SEM_CAMEL], t.[N_CAMEL]),
            ('VENTA_SEM_OTRASBAT', t.[VENTA_SEM_OTRASBAT], t.[N_OTRASBAT]),
            ('VENTA_SEM_MARLBORO', t.[VENTA_SEM_MARLBORO], t.[N_MARLBORO]),
            ('VENTA_SEM_BENSONHEADGES', t.[VENTA_SEM_BENSONHEADGES], t.[N_BENSONHEADGES]),
            ('VENTA_SEM_DELICADOS', t.[VENTA_SEM_DELICADOS], t.[N_DELICADOS]),
            ('VENTA_SEM_OTRASPMI', t.[VENTA_SEM_OTRASPMI], t.[N_OTRASPMI]),
            ('VENTA_SEM_OTRASMARCAS', t.[VENTA_SEM_OTRASMARCAS], t.[N_OTRASMARCAS])
        ) AS v([Campo], [Crudo], [Numero])
        WHERE NULLIF(LTRIM(RTRIM(v.[Crudo])), '') IS NOT NULL AND v.[Numero] IS NULL;

        INSERT INTO #Errores ([FILA], [CAMPO_ERROR], [MENSAJE_DETALLE])
        SELECT t.[FILA], 'NO_EN_INEGI', 'La encuesta no tiene coincidencia en ENCUESTAS_CLIENTES_INEGI'
        FROM #Trabajo AS t
        WHERE UPPER(LTRIM(RTRIM(t.[FUENTE]))) = 'INEGI'
          AND NOT EXISTS
          (
              SELECT 1 FROM [EAI].[ENCUESTAS_CLIENTES_INEGI] AS i
              WHERE i.[TERRITORIO] = t.[TERRITORIO]
                AND i.[SEMANA] = t.[SEMANA]
                AND i.[DIA_DE_VISITA] = t.[DIA_VISITA]
                AND i.[CONSECUTIVO] = t.[CONSECUTIVO]
          );

        SELECT [FILA], ROW_NUMBER() OVER (ORDER BY [FILA]) AS [ID]
        INTO #Invalidas
        FROM #Trabajo AS t
        WHERE EXISTS (SELECT 1 FROM #Errores AS e WHERE e.[FILA]=t.[FILA]);

        SELECT [FILA], ROW_NUMBER() OVER (ORDER BY [FILA]) AS [ID]
        INTO #Validas
        FROM #Trabajo AS t
        WHERE NOT EXISTS (SELECT 1 FROM #Errores AS e WHERE e.[FILA]=t.[FILA]);

        SET @Invalidos = (SELECT COUNT_BIG(*) FROM #Invalidas);
        SET @Validos = (SELECT COUNT_BIG(*) FROM #Validas);
        SET @InvalidosInegi = (SELECT COUNT_BIG(DISTINCT [FILA]) FROM #Errores WHERE [CAMPO_ERROR]='NO_EN_INEGI');

        INSERT INTO [EAI].[ENCUESTAS_CLIENTES_INVALIDAS]
        SELECT iv.[ID], t.[N_TERRITORIO],
               TRY_CONVERT(DECIMAL(6,0), CONCAT(DATEPART(YEAR,t.[FECHA_INICIO]), RIGHT('00'+CONVERT(VARCHAR(2),t.[N_SEMANA]),2))),
               CASE UPPER(LTRIM(RTRIM(t.[DIA_VISITA]))) WHEN 'LUNES' THEN 1 WHEN 'MARTES' THEN 2 WHEN 'MIERCOLES' THEN 3 WHEN 'MIÉRCOLES' THEN 3 WHEN 'JUEVES' THEN 4 WHEN 'VIERNES' THEN 5 WHEN 'SABADO' THEN 6 WHEN 'SÁBADO' THEN 6 END,
               t.[N_IDENCUESTADO], t.[N_IDENCUESTADOR], t.[FECHA_INICIO], t.[FECHA_FIN],
               LTRIM(RTRIM(t.[DIRECCIONIP])), LTRIM(RTRIM(t.[E_MAIL])), LTRIM(RTRIM(t.[NOMBRE])), LTRIM(RTRIM(t.[APELLIDO])), LTRIM(RTRIM(t.[DATOS_PERSONALIZADOS])),
               UPPER(LTRIM(RTRIM(t.[FUENTE]))), LTRIM(RTRIM(t.[VENDE_CIGARRO])), UPPER(LTRIM(RTRIM(t.[VISITADO_BAT]))), NULL,
               t.[N_PALLMALL],t.[N_LUCKYSTRIKE],t.[N_SHOTS],t.[N_CAMEL],t.[N_OTRASBAT],t.[N_MARLBORO],t.[N_BENSONHEADGES],t.[N_DELICADOS],t.[N_OTRASPMI],t.[N_OTRASMARCAS],
               LTRIM(RTRIM(t.[COORDENADAS])),UPPER(LTRIM(RTRIM(t.[TIENE_EXHIBIDOR]))),t.[N_TELEFONO],t.[N_SEMANA],UPPER(LTRIM(RTRIM(t.[DIA_VISITA]))),t.[N_CONSECUTIVO],
               LTRIM(RTRIM(t.[NOMBRE_CONTACTO])),LTRIM(RTRIM(t.[DATOS_GEN_NOMBRETIENDA])),LTRIM(RTRIM(t.[DATOS_GEN_CALLEYNUMERO])),LTRIM(RTRIM(t.[DATOS_GEN_COLONIAYCIUDAD])),
               t.[N_CODIGOPOSTAL],LTRIM(RTRIM(t.[DATOS_GEN_ESTADO])),UPPER(LTRIM(RTRIM(t.[CLIENTE_POTENCIAL]))),SYSDATETIME(),@Ejecucion
        FROM #Invalidas AS iv INNER JOIN #Trabajo AS t ON t.[FILA]=iv.[FILA];

        INSERT INTO [EAI].[ENCUESTAS_CLIENTES_ERRORES]
        SELECT ROW_NUMBER() OVER (ORDER BY e.[FILA],e.[CAMPO_ERROR]), iv.[ID], SYSDATETIME(), e.[MENSAJE_DETALLE], e.[CAMPO_ERROR]
        FROM #Errores AS e INNER JOIN #Invalidas AS iv ON iv.[FILA]=e.[FILA];

        INSERT INTO [EAI].[ENCUESTAS_CLIENTES_VALIDAS]
        SELECT va.[ID], t.[N_TERRITORIO],
               TRY_CONVERT(DECIMAL(6,0), CONCAT(DATEPART(YEAR,t.[FECHA_INICIO]), RIGHT('00'+CONVERT(VARCHAR(2),t.[N_SEMANA]),2))),
               CASE UPPER(LTRIM(RTRIM(t.[DIA_VISITA]))) WHEN 'LUNES' THEN 1 WHEN 'MARTES' THEN 2 WHEN 'MIERCOLES' THEN 3 WHEN 'MIÉRCOLES' THEN 3 WHEN 'JUEVES' THEN 4 WHEN 'VIERNES' THEN 5 WHEN 'SABADO' THEN 6 WHEN 'SÁBADO' THEN 6 END,
               t.[N_IDENCUESTADO], t.[N_IDENCUESTADOR], t.[FECHA_INICIO], t.[FECHA_FIN],
               LTRIM(RTRIM(t.[DIRECCIONIP])), LTRIM(RTRIM(t.[E_MAIL])), LTRIM(RTRIM(t.[NOMBRE])), LTRIM(RTRIM(t.[APELLIDO])), LTRIM(RTRIM(t.[DATOS_PERSONALIZADOS])),
               UPPER(LTRIM(RTRIM(t.[FUENTE]))), LTRIM(RTRIM(t.[VENDE_CIGARRO])), UPPER(LTRIM(RTRIM(t.[VISITADO_BAT]))), NULL,
               t.[N_PALLMALL],t.[N_LUCKYSTRIKE],t.[N_SHOTS],t.[N_CAMEL],t.[N_OTRASBAT],t.[N_MARLBORO],t.[N_BENSONHEADGES],t.[N_DELICADOS],t.[N_OTRASPMI],t.[N_OTRASMARCAS],
               LTRIM(RTRIM(t.[COORDENADAS])),UPPER(LTRIM(RTRIM(t.[TIENE_EXHIBIDOR]))),t.[N_TELEFONO],t.[N_SEMANA],UPPER(LTRIM(RTRIM(t.[DIA_VISITA]))),t.[N_CONSECUTIVO],
               LTRIM(RTRIM(t.[NOMBRE_CONTACTO])),LTRIM(RTRIM(t.[DATOS_GEN_NOMBRETIENDA])),LTRIM(RTRIM(t.[DATOS_GEN_CALLEYNUMERO])),LTRIM(RTRIM(t.[DATOS_GEN_COLONIAYCIUDAD])),
               t.[N_CODIGOPOSTAL],LTRIM(RTRIM(t.[DATOS_GEN_ESTADO])),UPPER(LTRIM(RTRIM(t.[CLIENTE_POTENCIAL]))),SYSDATETIME(),1
        FROM #Validas AS va INNER JOIN #Trabajo AS t ON t.[FILA]=va.[FILA];

        /* El body Oracle termina marcando todas las validas con ESTATUS=1. */
        UPDATE [EAI].[ENCUESTAS_CLIENTES_VALIDAS] SET [ESTATUS]=1;

        UPDATE c SET [PROCESADO]=1
        FROM [EAI].[ENCUESTAS_CLIENTES_CARGADAS] AS c
        INNER JOIN #Trabajo AS t ON t.[IDENCUESTADO]=c.[IDENCUESTADO]
        INNER JOIN #Validas AS v ON v.[FILA]=t.[FILA];

        INSERT INTO [EAI].[ENCUESTAS_CLIENTES_BITACORA]
        ([IDEJECUCION],[FECHA_HORA],[TIEMPO_EJECUCION],[ESTATUS],[CARGADOS],[DESCARTADOS_INEGI],[DESCARTADOS_ERRORES],[CARGADAS_VALIDAS],[MENSAJE])
        VALUES
        (@Ejecucion,SYSDATETIME(),CONCAT(DATEDIFF(MINUTE,@Inicio,SYSDATETIME())/60,':',RIGHT('00'+CONVERT(VARCHAR(2),DATEDIFF(MINUTE,@Inicio,SYSDATETIME())%60),2)),
         CASE WHEN @Invalidos>0 THEN 2 ELSE 1 END,@Cargados,@InvalidosInegi,@Invalidos,@Validos,NULL);

        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        DECLARE @Mensaje VARCHAR(2000)=CONCAT('Error ',ERROR_NUMBER(),' linea ',ERROR_LINE(),': ',ERROR_MESSAGE());
        IF XACT_STATE()<>0 ROLLBACK TRANSACTION;

        -- Oracle limpia toda la bitacora al iniciar; se conserva ese alcance aun
        -- cuando la transaccion principal haya sido revertida.
        DELETE FROM [EAI].[ENCUESTAS_CLIENTES_BITACORA];
        INSERT INTO [EAI].[ENCUESTAS_CLIENTES_BITACORA]
        ([IDEJECUCION],[FECHA_HORA],[TIEMPO_EJECUCION],[ESTATUS],[CARGADOS],[DESCARTADOS_INEGI],[DESCARTADOS_ERRORES],[CARGADAS_VALIDAS],[MENSAJE])
        VALUES (@Ejecucion,SYSDATETIME(),'0:00',CASE WHEN @Invalidos>0 THEN 4 ELSE 3 END,@Cargados,@InvalidosInegi,@Invalidos,@Validos,@Mensaje);

        THROW;
    END CATCH;
END;
GO
