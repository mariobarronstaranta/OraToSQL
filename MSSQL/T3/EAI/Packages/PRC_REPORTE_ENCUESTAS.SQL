/*
    Homologo de EAI.PKG_ENCUESTAS_MKT.PRC_REPORTE_ENCUESTAS.

    Dev Jr:
    - Reconstruye la tabla local de resumen a partir de CARGADAS y de los errores.
    - OUTER APPLY sustituye el SELECT INTO protegido por EXCEPTION de Oracle.
    - El database link @V3CEDIS se sustituye por el sinonimo
      [CEDIS].[ENCUESTAS_CLIENTES_RESUMEN]. La publicacion usa SQL dinamico para
      que este procedimiento pueda compilar antes de configurar el linked server.
    - Si falta el sinonimo, se genera un error deliberado: omitir la publicacion
      produciria un proceso aparentemente exitoso con datos incompletos.
*/
SET ANSI_NULLS ON;
SET QUOTED_IDENTIFIER ON;
GO

CREATE OR ALTER PROCEDURE [EAI].[PRC_REPORTE_ENCUESTAS]
AS
BEGIN
    SET NOCOUNT ON;
    SET XACT_ABORT ON;

    BEGIN TRY
        BEGIN TRANSACTION;

        DELETE FROM [EAI].[ENCUESTAS_CLIENTES_RESUMEN];

        INSERT INTO [EAI].[ENCUESTAS_CLIENTES_RESUMEN]
        (
            [TERRITORIO], [IDENCUESTADO], [IDENCUESTADOR], [FECHA_INICIO], [FECHA_FIN],
            [DIRECCIONIP], [E_MAIL], [NOMBRE], [APELLIDO], [DATOS_PERSONALIZADOS],
            [FUENTE], [VENDE_CIGARRO], [VISITADO_BAT], [CUANTO_VENDE],
            [VENTA_SEM_PALLMALL], [VENTA_SEM_LUCKYSTRIKE], [VENTA_SEM_SHOTS],
            [VENTA_SEM_CAMEL], [VENTA_SEM_OTRASBAT], [VENTA_SEM_MARLBORO],
            [VENTA_SEM_BENSONHEADGES], [VENTA_SEM_DELICADOS], [VENTA_SEM_OTRASPMI],
            [VENTA_SEM_OTRASMARCAS], [COORDENADAS], [TIENE_EXHIBIDOR], [TELEFONO],
            [SEMANA], [DIA_VISITA], [CONSECUTIVO], [NOMBRE_CONTACTO],
            [DATOS_GEN_NOMBRETIENDA], [DATOS_GEN_CALLEYNUMERO],
            [DATOS_GEN_COLONIAYCIUDAD], [DATOS_GEN_CODIGOPOSTAL], [DATOS_GEN_ESTADO],
            [CLIENTE_POTENCIAL], [VLD_NO_EN_INEGI], [VLD_TERRITORIO],
            [VLD_IDENCUESTADO], [VLD_IDENCUESTADOR], [VLD_FECHA_INICIO], [VLD_FECHA_FIN],
            [VLD_DIRECCIONIP], [VLD_E_MAIL], [VLD_NOMBRE], [VLD_APELLIDO],
            [VLD_DATOS_PERSONALIZADOS], [VLD_FUENTE], [VLD_VENDE_CIGARRO],
            [VLD_VISITADO_BAT], [VLD_CUANTO_VENDE], [VLD_VENTA_SEM_PALLMALL],
            [VLD_VENTA_SEM_LUCKYSTRIKE], [VLD_VENTA_SEM_SHOTS], [VLD_VENTA_SEM_CAMEL],
            [VLD_VENTA_SEM_OTRASBAT], [VLD_VENTA_SEM_MARLBORO],
            [VLD_VENTA_SEM_BENSONHEADGES], [VLD_VENTA_SEM_DELICADOS],
            [VLD_VENTA_SEM_OTRASPMI], [VLD_VENTA_SEM_OTRASMARCAS], [VLD_COORDENADAS],
            [VLD_TIENE_EXHIBIDOR], [VLD_TELEFONO], [VLD_SEMANA], [VLD_DIA_VISITA],
            [VLD_CONSECUTIVO], [VLD_NOMBRE_CONTACTO], [VLD_DATOS_GEN_NOMBRETIENDA],
            [VLD_DATOS_GEN_CALLEYNUMERO], [VLD_DATOS_GEN_COLONIAYCIUDAD],
            [VLD_DATOS_GEN_CODIGOPOSTAL], [VLD_DATOS_GEN_ESTADO],
            [VLD_CLIENTE_POTENCIAL], [INEGI_NOM_ESTAB], [INEGI_CALLE],
            [INEGI_NUMERO_EXT], [INEGI_COLONIA], [INEGI_COD_POSTAL], [INEGI_ENTIDAD],
            [INEGI_MUNICIPIO], [INEGI_LOCALIDAD], [INEGI_TELEFONO], [INEGI_LATITUD],
            [INEGI_LONGITUD], [INEGI_ID_BAT], [INEGI_CLAVE], [SUPERVISOR]
        )
        SELECT
            c.[TERRITORIO], c.[IDENCUESTADO], c.[IDENCUESTADOR], c.[FECHA_INICIO], c.[FECHA_FIN],
            c.[DIRECCIONIP], c.[E_MAIL], c.[NOMBRE], c.[APELLIDO], c.[DATOS_PERSONALIZADOS],
            c.[FUENTE], c.[VENDE_CIGARRO], c.[VISITADO_BAT], c.[CUANTO_VENDE],
            c.[VENTA_SEM_PALLMALL], c.[VENTA_SEM_LUCKYSTRIKE], c.[VENTA_SEM_SHOTS],
            c.[VENTA_SEM_CAMEL], c.[VENTA_SEM_OTRASBAT], c.[VENTA_SEM_MARLBORO],
            c.[VENTA_SEM_BENSONHEADGES], c.[VENTA_SEM_DELICADOS], c.[VENTA_SEM_OTRASPMI],
            c.[VENTA_SEM_OTRASMARCAS], c.[COORDENADAS], c.[TIENE_EXHIBIDOR], c.[TELEFONO],
            c.[SEMANA], c.[DIA_VISITA], c.[CONSECUTIVO], c.[NOMBRE_CONTACTO],
            c.[DATOS_GEN_NOMBRETIENDA], c.[DATOS_GEN_CALLEYNUMERO],
            c.[DATOS_GEN_COLONIAYCIUDAD], c.[DATOS_GEN_CODIGOPOSTAL], c.[DATOS_GEN_ESTADO],
            c.[CLIENTE_POTENCIAL],
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('NO_EN_INEGI', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('TERRITORIO', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('IDENCUESTADO', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('IDENCUESTADOR', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('FECHA_INICIO', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('FECHA_FIN', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('DIRECCIONIP', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('E_MAIL', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('NOMBRE', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('APELLIDO', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('DATOS_PERSONALIZADOS', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('FUENTE', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('VENDE_CIGARRO', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('VISITADO_BAT', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('CUANTO_VENDE', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('VENTA_SEM_PALLMALL', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('VENTA_SEM_LUCKYSTRIKE', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('VENTA_SEM_SHOTS', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('VENTA_SEM_CAMEL', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('VENTA_SEM_OTRASBAT', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('VENTA_SEM_MARLBORO', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('VENTA_SEM_BENSONHEADGES', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('VENTA_SEM_DELICADOS', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('VENTA_SEM_OTRASPMI', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('VENTA_SEM_OTRASMARCAS', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('COORDENADAS', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('TIENE_EXHIBIDOR', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('TELEFONO', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('SEMANA', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('DIA_VISITA', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('CONSECUTIVO', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('NOMBRE_CONTACTO', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('DATOS_GEN_NOMBRETIENDA', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('DATOS_GEN_CALLEYNUMERO', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('DATOS_GEN_COLONIAYCIUDAD', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('DATOS_GEN_CODIGOPOSTAL', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('DATOS_GEN_ESTADO', x.IDENCUESTADO),
            [EAI].[FNC_REVISA_CAMPO_VALIDO]('CLIENTE_POTENCIAL', x.IDENCUESTADO),
            i.[NOM_ESTAB], i.[CALLE], i.[NUMERO_EXT], i.[COLONIA], i.[COD_POSTAL],
            i.[ENTIDAD], i.[MUNICIPIO], i.[LOCALIDAD], i.[TELEFONO], i.[LATITUD],
            i.[LONGITUD], i.[ID_BAT], i.[CLAVE], c.[SUPERVISOR]
        FROM [EAI].[ENCUESTAS_CLIENTES_CARGADAS] AS c
        CROSS APPLY (VALUES (COALESCE(TRY_CONVERT(DECIMAL(30, 0), NULLIF(LTRIM(RTRIM(c.[IDENCUESTADO])), '')), 0))) AS x(IDENCUESTADO)
        OUTER APPLY
        (
            SELECT TOP (1) e.[NOM_ESTAB], e.[CALLE], e.[NUMERO_EXT], e.[COLONIA],
                   e.[COD_POSTAL], e.[ENTIDAD], e.[MUNICIPIO], e.[LOCALIDAD],
                   e.[TELEFONO], e.[LATITUD], e.[LONGITUD], e.[ID_BAT], e.[CLAVE]
            FROM [EAI].[ENCUESTAS_CLIENTES_INEGI] AS e
            WHERE c.[FUENTE] = 'INEGI'
              AND e.[TERRITORIO] = c.[TERRITORIO]
              AND e.[SEMANA] = c.[SEMANA]
              AND e.[DIA_DE_VISITA] = c.[DIA_VISITA]
              AND e.[CONSECUTIVO] = c.[CONSECUTIVO]
        ) AS i;

        COMMIT TRANSACTION;

        IF NOT EXISTS
        (
            SELECT 1 FROM sys.synonyms
            WHERE [schema_id] = SCHEMA_ID(N'CEDIS')
              AND [name] = N'ENCUESTAS_CLIENTES_RESUMEN'
        )
        BEGIN
            ;THROW 50020, 'Falta configurar el sinonimo [CEDIS].[ENCUESTAS_CLIENTES_RESUMEN] para sustituir @V3CEDIS.', 1;
        END;

        DECLARE @Columnas NVARCHAR(MAX) = N'';
        DECLARE @Sql NVARCHAR(MAX);

        SELECT @Columnas = @Columnas + CASE WHEN @Columnas = N'' THEN N'' ELSE N', ' END
                          + N'r.' + QUOTENAME([name])
        FROM sys.columns
        WHERE [object_id] = OBJECT_ID(N'[EAI].[ENCUESTAS_CLIENTES_RESUMEN]')
          AND [name] <> N'SUPERVISOR'
        ORDER BY [column_id];

        SET @Sql = N'DELETE FROM [CEDIS].[ENCUESTAS_CLIENTES_RESUMEN];
            INSERT INTO [CEDIS].[ENCUESTAS_CLIENTES_RESUMEN]
            SELECT ' + @Columnas + N',
                   CASE WHEN EXISTS
                   (
                       SELECT 1 FROM [EAI].[ENCUESTAS_CLIENTES_INVALIDAS] AS iv
                       WHERE iv.[TERRITORIO] = TRY_CONVERT(DECIMAL(10,0), r.[TERRITORIO])
                         AND iv.[IDENCUESTADO] = TRY_CONVERT(DECIMAL(30,0), r.[IDENCUESTADO])
                   ) THEN 0 ELSE 1 END AS [VALIDA],
                   r.[SUPERVISOR]
            FROM [EAI].[ENCUESTAS_CLIENTES_RESUMEN] AS r;';

        EXEC sys.sp_executesql @Sql;
    END TRY
    BEGIN CATCH
        IF XACT_STATE() <> 0 ROLLBACK TRANSACTION;
        THROW;
    END CATCH;
END;
GO
